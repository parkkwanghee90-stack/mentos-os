function lb(key, find, replace) {
  if (!texts[key]) { console.log('NOT FOUND: ' + key); return; }
  if (!texts[key].includes(find)) { console.log('PATTERN NOT FOUND in ' + key + ': ' + find.substring(0, 40)); return; }
  texts[key] = texts[key].replace(find, replace);
  console.log('Fixed: ' + key);
}

