export function getMonthKey(date = new Date()) {
  let month = date;
  if(date instanceof Date) month = date.getMonth() + 1;
  else if (typeof date === 'number') month = date;

  const map = {
    1: "march",
    2: "march",
    3: "march",
    4: "april",
    5: "may",
    6: "june",
    7: "july",
    8: "september",
    9: "september",
    10: "october",
    11: "november",
    12: "november",
  };
  return map[month] || "march";
}

export function getStudyMode(date = new Date()) {
  let month = date;
  let day = 15;
  if(date instanceof Date) {
      month = date.getMonth() + 1;
      day = date.getDate();
  }

  if ((month === 4 && day >= 20) || (month === 5 && day <= 10)) return "EXAM_MID";
  if ((month === 6 && day >= 20) || (month === 7 && day <= 10)) return "EXAM_FINAL";
  if ((month === 9 && day >= 20) || (month === 10 && day <= 10)) return "EXAM_MID2";
  if ((month === 11 && day >= 20) || (month === 12 && day <= 10)) return "EXAM_FINAL2";

  return "NORMAL";
}

export const parseGrade = (gradeStr) => {
  if (!gradeStr) return 'middle1';
  const map = {
    '중1': 'middle1', '중2': 'middle2', '중3': 'middle3',
    '고1': 'high1', '고2': 'high2', '고3': 'high3'
  };
  return map[gradeStr] || 'middle1';
};

export const parseSubject = (subjectStr) => {
  if (!subjectStr) return 'physics';
  if (subjectStr === '수학' || subjectStr === 'math') return 'math';
  if (subjectStr === '영어' || subjectStr === 'english') return 'english';
  if (subjectStr === '물리' || subjectStr === 'physics1' || subjectStr === '물리학1' || subjectStr === 'physics') return 'physics';
  if (subjectStr === '화학' || subjectStr === 'chemistry1' || subjectStr === '화학1' || subjectStr === 'chemistry') return 'chemistry';
  if (subjectStr === '생명과학' || subjectStr === 'biology1' || subjectStr === '생명1' || subjectStr === 'biology') return 'biology';
  if (subjectStr === '지구과학' || subjectStr === 'earth_science1' || subjectStr === '지학' || subjectStr === 'earth_science' || subjectStr === 'earthScience') return 'earthScience';
  return 'physics'; // default
};

export function getCurrentUnit({ grade, subject, month, date = new Date() }) {
  const g = parseGrade(grade);
  const s = parseSubject(subject);
  // Support both old month argument and new Date() argument
  const monthKey = getMonthKey(month || date);
  
  return curriculumScience?.[g]?.[s]?.[monthKey] || null;
}

